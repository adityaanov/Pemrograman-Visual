    public void totalbiaya(){
        int jumlahbaris = jTable1.getRowCount();
        int totalbiaya = 0;
        int jumlahmenu, hargamenu;
        for (int i = 0; i <jumlahbaris; i++){
            jumlahmenu = Integer.parseInt(jTable1.getValueAt(i, 3).toString());
            hargamenu = Integer.parseInt(jTable1.getValueAt(i, 4).toString());
            totalbiaya = totalbiaya + (jumlahmenu + hargamenu);
        }
        txt_totalbayar.setText(String.valueOf(totalbiaya));
        txt_tampil.setText("Rp "+ totalbiaya +",-");
    }
    
    public void loadData(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{
        txt_notransaksi.getText(),
        txt_idmenu.getText(),
        txt_namamenu.getText(),
        txt_jumlah.getText(),
        txt_harga.getText(),
        txt_totalbayar.getText(),
    });
    }
    
    public void kosong(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        while (model.getRowCount()>0){
            model.removeRow(0);
        }
    }
    
