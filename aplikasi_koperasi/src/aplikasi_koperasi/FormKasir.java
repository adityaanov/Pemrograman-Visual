    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        String no_faktur = txt_notransaksi.getText();
        String tanggal = txt_tanggal.getText();
        String id_pelanggan = txt_idpelanggan.getText();
        String total = txt_totalbayar.getText();
        
        try{
            Connection c = Koneksi.KoneksiDatabase();
            String sql = "INSERT INTO kasir2 VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, no_faktur);
            p.setString(2, tanggal);
            p.setString(3, id_pelanggan);
            p.setString(4, total);
            p.close();
        }catch (Exception e){
            System.out.println("Simpan Penjualan Error");
        }
        
        try{
            Connection c = Koneksi.KoneksiDatabase();
            int baris = jTable1.getRowCount();
            for (int i = 0; i < baris; i++){
                String sql = "INSERT INTO kasir3(no_faktur, id_menu, nama_menu, jumlah, harga, total)VALUES('"
                        +jTable1.getValueAt(i, 0) +"', '"+jTable1.getValueAt(i, 1) +"', '"+jTable1.getValueAt(i, 2) +"', '"
                        +jTable1.getValueAt(i, 3) +"', '"+jTable1.getValueAt(i, 4) +"', '"+jTable1.getValueAt(i, 5) +"', '";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        }catch (Exception e){
            System.out.println("Simpan Penjualan Error");
        }
        clear();
        utama();
        kosong();
        txt_tampil.setText("Rp 0,-");
    }//GEN-LAST:event_btn_simpanActionPerformed

