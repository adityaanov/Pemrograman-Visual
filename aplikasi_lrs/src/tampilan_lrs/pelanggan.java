    private void tabelpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpelangganMouseClicked
        // TODO add your handling code here:
        int bar = tabelpelanggan.getSelectedRow();
        String a = tabelpelanggan.getValueAt(bar, 0).toString();
        String b = tabelpelanggan.getValueAt(bar, 1).toString();
        String c = tabelpelanggan.getValueAt(bar, 2).toString();
        String d = tabelpelanggan.getValueAt(bar, 3).toString();
        String e = tabelpelanggan.getValueAt(bar, 4).toString();
        
        txtid.setText(a);
        txtnm.setText(b);
        if ("Laki-Laki".equals(c)){
            rlaki.setSelected(true);
        }else{
            rperempuan.setSelected(true);
        }
        txttelp.setText(d);
        txtalamat.setText(e);
    }//GEN-LAST:event_tabelpelangganMouseClicked

